/**
 * @brief State machine operation type
 */
typedef enum
{
    SM_OP_NONE = 0,         // No operation
    SM_OP_PROGRAM,          // Programming operation
    SM_OP_CRC_CHECK,        // CRC check operation
} SM_OPERATION;

/**
 * @brief Unified state machine states (for concurrent programming and CRC check)
 */
typedef enum
{
    SM_STATE_IDLE = 0,          // Idle state, ready to start
    SM_STATE_READ_FLASH,        // Reading firmware from flash (program only)
    SM_STATE_PREPARE_CMD,       // Preparing UART command
    SM_STATE_TX_STARTING,       // Starting UART TX/RX DMA
    SM_STATE_WAIT_TX,           // Waiting for TX DMA complete
    SM_STATE_WAIT_RX,           // Waiting for RX DMA complete  
    SM_STATE_CHECK_ACK,         // Checking ACK response
    SM_STATE_VERIFY_CRC,        // Final CRC comparison (CRC only)
    SM_STATE_COMPLETE,          // Operation complete
    SM_STATE_ERROR,             // Error occurred
} SM_STATE;

// Keep old names for compatibility
