#define SLAVE_FLASH_CHIP_0_MAP_0_CONFIG_A_ADDR          (0x08004000)
#define SLAVE_FLASH_CHIP_0_MAP_0_CONFIG_B_ADDR          (0x08005000)
#define SLAVE_FLASH_CHIP_0_MAP_0_FW_BIN_A_ADDR          (0x08008000)
#define SLAVE_FLASH_CHIP_0_MAP_0_FW_BIN_B_ADDR          (0x08020000)
#define SLAVE_FLASH_CHIP_0_MAP_0_DEBUG_ADDR             (0x08021000)
#define SLAVE_FLASH_CHIP_0_MAP_0_MAX_ADDR               (0x08040000)


#define SLAVE_FLASH_OP_ADDR_SIZE            (4)

/////////////////// SLAVE FLASH STATUS
