// CHIP id 0 : FL100, MAP 0 : memmap version 0
// AUTHOR: authorization info, like rollcode, triple, motor encryption key, etc
// CLIENT: client info
// CONFIG_A, CONFIG_B: configuration data bin partition A and B, generated by automatic compile tool
// FW_BIN_A, FW_BIN_B: fw bin partition A and B
// DEBUG: for wirte debug, could P/E without quit WP operation
