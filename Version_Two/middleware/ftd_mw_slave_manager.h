/**
 * @brief Unified state machine states (for concurrent programming and CRC check)
 */
typedef enum
{
    SM_STATE_IDLE = 0,          // Idle state, ready to start

    // Boot trigger specific states
    SM_STATE_POWER_OFF,         // Power off slave (boot only)
    SM_STATE_WAIT_POWER_OFF,    // Waiting for power off stable (boot only)
    SM_STATE_POWER_ON,          // Power on slave (boot only)
    SM_STATE_WAIT_POWER_STABLE, // Waiting for power stable (boot only)
    SM_STATE_RX_START,          // Start UART RX (boot only)
    SM_STATE_TX_CMD,            // Send trigger command (boot only)
    SM_STATE_EXTENDED_WAIT,     // Extended wait for response (boot only)

    // Program/CRC common states
    SM_STATE_READ_FLASH,        // Reading firmware from flash (program only)
    SM_STATE_PREPARE_CMD,       // Preparing UART command
    SM_STATE_TX_STARTING,       // Starting UART TX/RX DMA
    SM_STATE_WAIT_TX,           // Waiting for TX DMA complete
    SM_STATE_WAIT_RX,           // Waiting for RX DMA complete  
    SM_STATE_CHECK_ACK,         // Checking ACK response
    SM_STATE_VERIFY_CRC,        // Final CRC comparison (CRC only)

    // Calibration/Test specific states
    SM_STATE_WAIT_VALUE,        // Waiting for calibration value response
    SM_STATE_PARSE_VALUE,       // Parsing calibration value
    SM_STATE_MEASURE_CURRENT,   // Measuring current (sleep/static)
    SM_STATE_CALIBRATE_VALUE,   // Calibrating value (placeholder)


    // Terminal states
    SM_STATE_COMPLETE,          // Operation complete
    SM_STATE_ERROR,             // Error occurred
} SM_STATE;

