// Chip flash address map structure
typedef struct
{
    const char* chip_name;      // Chip name (e.g., "FM124AM")
    uint16_t    chip_code;      // Chip code (e.g., 0x2202)
    CHIP_FAMILY chip_family;    // Chip family (FL100/FL100B)
    // 2M/4M Flash addresses
    uint32_t    fw_bin_a_addr;  // FW partition A start address
    uint32_t    fw_bin_b_addr;  // FW partition B start address
    uint32_t    fw_size;        // FW partition size (bytes)
    // 1M Flash addresses (0 if not supported)
    uint32_t    fw_bin_a_addr_1m; // 1M Flash FW partition A start address
    uint32_t    fw_bin_b_addr_1m; // 1M Flash FW partition B start address
    uint32_t    fw_size_1m;       // 1M Flash FW partition size (bytes)
    // Config partition addresses
    uint32_t    config_a_addr;  // Config partition A address
    uint32_t    config_b_addr;  // Config partition B address
    uint32_t    config_size;    // Config partition size (bytes)
    // Authorization info addresses
    uint32_t    author_addr;    // Rollcode/PID/Triple address
    uint32_t    author_size;    // Authorization info size (bytes)
    uint32_t 	burn_info_addr; // Burn info address
    uint32_t    burn_info_size; // Burn info size (bytes)
} CHIP_FLASH_ADDR_MAP;

